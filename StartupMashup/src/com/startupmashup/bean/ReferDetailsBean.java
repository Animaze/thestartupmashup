package com.startupmashup.bean;

public class ReferDetailsBean extends BaseBean{

	private String name;
	private String phoneNumber;
	private String emailId;
	private String collegeName;
	private int hackathonId;
	private String refName;
	private String refEmailId;
	private String refPhoneNumber;
	

	

	public String getRefName() {
		return refName;
	}

	public void setRefName(String refName) {
		this.refName = refName;
	}

	public String getRefEmailId() {
		return refEmailId;
	}

	public void setRefEmailId(String refEmailId) {
		this.refEmailId = refEmailId;
	}

	public String getRefPhoneNumber() {
		return refPhoneNumber;
	}

	public void setRefPhoneNumber(String refPhoneNumber) {
		this.refPhoneNumber = refPhoneNumber;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		
		this.name = name;
	}

	public String getPhoneNumber() {
		return phoneNumber;
	}

	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}

	public String getEmailId() {
		return emailId;
	}

	public void setEmailId(String emailId) {
		this.emailId = emailId;
	}

	public String getCollegeName() {
		return collegeName;
	}

	public void setCollegeName(String collegeName) {
		this.collegeName = collegeName;
	}

	public int getHackathonId() {
		return hackathonId;
	}

	public void setHackathonId(int hackathonId) {
		this.hackathonId = hackathonId;
	}

	

}
